public class Menu {
    private String menu_Name;
    private double menu_Price;
    
    public Menu(String menu_Name, double menu_Price) {
        this.menu_Name = menu_Name;
        this.menu_Price = menu_Price;
    }

    public String getmenu_Name() {
        return menu_Name;
    }

    public double getmenu_Price() {
        return menu_Price;
    }

}

    